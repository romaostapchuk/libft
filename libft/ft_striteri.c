#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	char	*ss;

	if (!(s && f))
		return ;
	ss = s;
	while (*ss)
	{
		f(ss - s, &*ss);
		ss++;
	}
}
